// Computed properties for navigation
const showLeftArrow = computed(() => {
  if (!sliderContainer.value) return false;
  return sliderContainer.value.scrollLeft > 0;
});

const showRightArrow = computed(() => {
  if (!sliderContainer.value) return false;
  const { scrollLeft, scrollWidth, clientWidth } = sliderContainer.value;
  return scrollLeft < scrollWidth - clientWidth - 10;
});

const visibleDots = computed(() => {
  const totalCategories = props.categories.length;
  const categoriesPerView = 4; // Assuming 4 categories visible at once
  return Math.ceil(totalCategories / categoriesPerView);
});

// Scroll functions
const scrollLeft = () => {
  if (!sliderContainer.value || isScrolling.value) return;
  
  isScrolling.value = true;
  const scrollAmount = 320; // Width of one category card + gap
  sliderContainer.value.scrollBy({
    left: -scrollAmount,
    behavior: 'smooth'
  });
  
  setTimeout(() => {
    isScrolling.value = false;
  }, 500);
};

const scrollRight = () => {
  if (!sliderContainer.value || isScrolling.value) return;
  
  isScrolling.value = true;
  const scrollAmount = 320; // Width of one category card + gap
  sliderContainer.value.scrollBy({
    left: scrollAmount,
    behavior: 'smooth'
  });
  
  setTimeout(() => {
    isScrolling.value = false;
  }, 500);
};

const scrollToIndex = (index) => {
  if (!sliderContainer.value || isScrolling.value) return;
  
  isScrolling.value = true;
  const scrollAmount = 320 * index; // Width of one category card + gap
  sliderContainer.value.scrollTo({
    left: scrollAmount,
    behavior: 'smooth'
  });
  
  setTimeout(() => {
    isScrolling.value = false;
  }, 500);
};

const handleScroll = () => {
  if (!sliderContainer.value) return;
  
  const { scrollLeft, scrollWidth, clientWidth } = sliderContainer.value;
  const maxScroll = scrollWidth - clientWidth;
  const scrollPercentage = scrollLeft / maxScroll;
  
  // Update current dot index based on scroll position
  const categoriesPerView = 4;
  const totalDots = Math.ceil(props.categories.length / categoriesPerView);
  currentDotIndex.value = Math.round(scrollPercentage * (totalDots - 1));
};

// Lifecycle
onMounted(() => {
  // Add scroll event listener
  if (sliderContainer.value) {
    sliderContainer.value.addEventListener('scroll', handleScroll);
  }
});

onUnmounted(() => {
  // Remove scroll event listener
  if (sliderContainer.value) {
    sliderContainer.value.removeEventListener('scroll', handleScroll);
  }
});
